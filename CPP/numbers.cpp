#include <iostream>

using namespace std;

int main() {

  // int
  int myNum = 1000;
  cout << myNum;

  // float
  float myFloat = 5.75;
  cout << myFloat;

  // double
  double myDouble = 19.99;
  cout << myDouble;  

  // scientific numbers
  float f1 = 35e3;
  double d1 = 12E4;
  cout << f1;
  cout << d1;




  return 0;
}